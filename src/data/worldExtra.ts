import { Project, ProjectType } from "@/types";

interface WIn {
  id: string;
  en: string;
  ge?: string;
  org: string;
  orgId: string;
  type: ProjectType;
  country: string;
  cc: string;
  city: string;
  lat: number;
  lng: number;
  deadline: string;
  start: string;
  end: string;
  ageMin?: number;
  ageMax?: number;
  cost?: number | "free";
  currency?: string;
  grant?: boolean;
  grantAmount?: number;
  desc: string;
  descGe?: string;
  req?: string[];
  langs?: string[];
  spots?: number;
  url: string;
  apply?: string;
  tags: string[];
}

function w(p: WIn): Project {
  return {
    id: p.id,
    title: p.ge ?? p.en,
    titleEn: p.en,
    organization: p.org,
    type: p.type,
    scope: "world",
    country: p.country,
    countryCode: p.cc,
    city: p.city,
    lat: p.lat,
    lng: p.lng,
    deadline: p.deadline,
    startDate: p.start,
    endDate: p.end,
    ageMin: p.ageMin ?? 18,
    ageMax: p.ageMax ?? 30,
    cost: p.cost ?? "free",
    currency: p.currency,
    grantCovered: p.grant ?? false,
    grantAmount: p.grantAmount,
    description: p.desc,
    descriptionGe: p.descGe,
    requirements: p.req ?? [],
    languages: p.langs ?? ["English"],
    spots: p.spots,
    sourceUrl: p.url,
    applyUrl: p.apply ?? p.url,
    sourceOrg: p.orgId,
    tags: p.tags,
    createdAt: "2026-06-01",
    updatedAt: "2026-06-21",
  };
}

