export const GEORGIA_BOUNDS = {
  minLng: 39.986,
  maxLng: 46.6948,
  minLat: 41.0441,
  maxLat: 43.5758,
};

/** Ray-casting point-in-polygon test (lng/lat space). */
export function pointInGeorgia(lng: number, lat: number): boolean {
  const poly = GEORGIA_BORDER;
  let inside = false;
  for (let i = 0, j = poly.length - 1; i < poly.length; j = i++) {
    const [xi, yi] = poly[i];
    const [xj, yj] = poly[j];
    const intersect =
      yi > lat !== yj > lat &&
      lng < ((xj - xi) * (lat - yi)) / (yj - yi) + xi;
    if (intersect) inside = !inside;
  }
  return inside;
}
